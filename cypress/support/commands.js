Cypress.Commands.add('selectProduct', (sendProductName) => { 
    cy.get('.card-title').each((productName, index)=>{
        const product_name = productName.text()
        if(product_name.includes(sendProductName))
        {
            cy.get('.btn.btn-info').eq(index).click()
            
        }
    }) })

